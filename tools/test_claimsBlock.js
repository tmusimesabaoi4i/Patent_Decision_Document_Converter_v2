/**
 * test_claimsBlock.js
 * --------------------------------------------------------------------------
 * stripBlankLinesInClaimsBlock の関数単体テスト（Node.js 標準ライブラリのみ）。
 *
 * 仕様: 「・請求項（／・引用文献等／・備考）」のヘッダ群から次の同型ヘッダ群
 * までの本文の空行を全削除し、次のヘッダ群の直前に空行をちょうど 1 行残す。
 *
 * 使い方: node tools/test_claimsBlock.js   （失敗時は exit 1）
 */

"use strict";

const fs = require("fs");
const path = require("path");
const vm = require("vm");
const assert = require("assert");

const ROOT = path.resolve(__dirname, "..");
// formatBody.js は isHeadingLine（見出し前の空行保持の判定）のために必要
for (const src of ["js/textPrimitives.js", "js/formatBody.js", "js/stripBlankLines.js"]) {
  vm.runInThisContext(fs.readFileSync(path.join(ROOT, src), "utf8"), { filename: src });
}

const fn = globalThis.stripBlankLines.stripBlankLinesInClaimsBlock;
assert.strictEqual(typeof fn, "function", "stripBlankLinesInClaimsBlock がエクスポートされていない");

let passed = 0;
function check(name, input, expected) {
  const actual = fn(input);
  try {
    assert.strictEqual(actual, expected);
    passed++;
  } catch (_e) {
    console.error(`FAIL: ${name}`);
    console.error("--- 入力 ---");
    console.error(JSON.stringify(input));
    console.error("--- 期待 ---");
    console.error(JSON.stringify(expected));
    console.error("--- 実際 ---");
    console.error(JSON.stringify(actual));
    process.exit(1);
  }
}

// ---------------------------------------------------------------------------
// ユーザー仕様の4パターン（逐語）
// ---------------------------------------------------------------------------

// ① 請求項 + 引用文献等 + 備考
check(
  "パターン①: 3行ヘッダ群",
  "・請求項\n・引用文献等\n・備考\nXXX\n\nXXX\n\nXXX\n\n・請求項\n・引用文献等\n・備考",
  "・請求項\n・引用文献等\n・備考\nXXX\nXXX\nXXX\n\n・請求項\n・引用文献等\n・備考"
);

// ② 請求項 + 引用文献等
check(
  "パターン②: 2行ヘッダ群（引用文献等）",
  "・請求項\n・引用文献等\nXXX\n\nXXX\n\nXXX\n\n・請求項\n・引用文献等",
  "・請求項\n・引用文献等\nXXX\nXXX\nXXX\n\n・請求項\n・引用文献等"
);

// ③ 請求項 + 備考
check(
  "パターン③: 2行ヘッダ群（備考）",
  "・請求項\n・備考\nXXX\n\nXXX\n\nXXX\n\n・請求項\n・備考",
  "・請求項\n・備考\nXXX\nXXX\nXXX\n\n・請求項\n・備考"
);

// ④ 請求項のみ
check(
  "パターン④: 1行ヘッダ群",
  "・請求項\nXXX\n\nXXX\n\nXXX\n\n・請求項",
  "・請求項\nXXX\nXXX\nXXX\n\n・請求項"
);

// ---------------------------------------------------------------------------
// エッジケース
// ---------------------------------------------------------------------------

// 番号付きヘッダ（行頭一致）
check(
  "番号付きヘッダ行も対象（行頭一致）",
  "・請求項　１－３\n・引用文献等　１－２\n・備考\n本文Ａ\n\n本文Ｂ\n\n・請求項　４\n・引用文献等　３",
  "・請求項　１－３\n・引用文献等　１－２\n・備考\n本文Ａ\n本文Ｂ\n\n・請求項　４\n・引用文献等　３"
);

// 3ブロック連続 → 両方の間隙が処理される
check(
  "3ブロック連続で全間隙を処理",
  "・請求項\nAAA\n\nAAA\n\n・請求項\nBBB\n\nBBB\n\n・請求項",
  "・請求項\nAAA\nAAA\n\n・請求項\nBBB\nBBB\n\n・請求項"
);

// 閉じヘッダの無い末尾本文は不変
check(
  "最後のヘッダ群の後ろは対象外",
  "・請求項\nXXX\n\nXXX\n\n・請求項\n末尾本文\n\n末尾本文",
  "・請求項\nXXX\nXXX\n\n・請求項\n末尾本文\n\n末尾本文"
);

// ヘッダ群が隣接（本文なし）→ 不変
check(
  "本文なしの隣接ヘッダ群は不変",
  "・請求項\n・備考\n・請求項\n・備考",
  "・請求項\n・備考\n・請求項\n・備考"
);

// ヘッダ群の間が空行のみ → 不変（空行だけの本文に余計な変更をしない）
check(
  "空行のみの本文は不変",
  "・請求項\n\n\n・請求項",
  "・請求項\n\n\n・請求項"
);

// 前後に無関係のテキストがあっても対象範囲のみ処理
check(
  "対象範囲外は不変",
  "前文\n\n前文\n・請求項\nXXX\n\nXXX\n\n・請求項\n・備考\n後文\n\n後文",
  "前文\n\n前文\n・請求項\nXXX\nXXX\n\n・請求項\n・備考\n後文\n\n後文"
);

// 全角スペースのみの行も空行として削除される
check(
  "全角スペースのみの行も空行扱い",
  "・請求項\nXXX\n　\nXXX\n\n・請求項",
  "・請求項\nXXX\nXXX\n\n・請求項"
);

// ヘッダ群が1つも無い / 空入力
check("ヘッダ無しは不変", "ただの本文\n\nただの本文", "ただの本文\n\nただの本文");
check("空文字は空文字", "", "");
assert.strictEqual(fn(null), "", "null は空文字を返す");
passed++;

// ---------------------------------------------------------------------------
// 終端条件の拡張: ●理由行（ユーザー仕様の逐語ケース）
// ---------------------------------------------------------------------------

// ●理由行で挟まれたブロック（3行ヘッダ群）
check(
  "●理由終端: 3行ヘッダ群（仕様例の逐語）",
  "●理由Ｎについて\n・請求項\n・引用文献等\n・備考\nXXXX\n\nXXXX\n\nXXXX\n\n●理由Ｎについて",
  "●理由Ｎについて\n・請求項\n・引用文献等\n・備考\nXXXX\nXXXX\nXXXX\n\n●理由Ｎについて"
);

// 4パターン × ●理由終端
check(
  "●理由終端: 請求項+引用文献等",
  "・請求項\n・引用文献等\nXXXX\n\nXXXX\n\n●理由２について",
  "・請求項\n・引用文献等\nXXXX\nXXXX\n\n●理由２について"
);
check(
  "●理由終端: 請求項+備考",
  "・請求項\n・備考\nXXXX\n\nXXXX\n\n●理由２について",
  "・請求項\n・備考\nXXXX\nXXXX\n\n●理由２について"
);
check(
  "●理由終端: 請求項のみ",
  "・請求項\nXXXX\n\nXXXX\n\n●理由２について",
  "・請求項\nXXXX\nXXXX\n\n●理由２について"
);

// 複数理由セクションの現実形（●理由境界を越えて詰めない回帰テスト）
check(
  "複数●理由セクションで全ブロック処理・境界を越えない",
  "●理由１（新規性）について\n・請求項　１\n・引用文献等　１\nAAA\n\nAAA\n\n●理由２（進歩性）について\n・請求項　２\n・備考\nBBB\n\nBBB\n\n・請求項　３\nCCC\n\nCCC\n\n●理由３について",
  "●理由１（新規性）について\n・請求項　１\n・引用文献等　１\nAAA\nAAA\n\n●理由２（進歩性）について\n・請求項　２\n・備考\nBBB\nBBB\n\n・請求項　３\nCCC\nCCC\n\n●理由３について"
);

// ●理由以外の●行では終端しない（本文の一部として残り、次の終端まで詰める）
check(
  "●理由以外の●行は終端にならない",
  "・請求項\nXXX\n\n●参考\n\nYYY\n\n●理由２について",
  "・請求項\nXXX\n●参考\nYYY\n\n●理由２について"
);

// ヘッダ群→空行のみ→●理由 は不変
check(
  "●理由の直前に本文が無ければ不変",
  "・請求項\n・備考\n\n●理由２について",
  "・請求項\n・備考\n\n●理由２について"
);

// 番号・括弧付きの●理由見出しも終端
check(
  "括弧付き●理由見出しも終端",
  "・請求項　１－３\nXXX\n\nXXX\n\n●理由１（新規性）について",
  "・請求項　１－３\nXXX\nXXX\n\n●理由１（新規性）について"
);

// ---------------------------------------------------------------------------
// 終端条件の拡張: 山括弧見出し行（最終ブロック対策）
// ---------------------------------------------------------------------------

// 半角 < 見出しで終端
check(
  "山括弧見出し（半角）で終端",
  "・請求項　７\n・引用文献等　４\n・備考\nXXX\n\nXXX\n\n<引用文献等一覧>\n１．特開２０２０－１２３４５６号公報\n\n２．特開２０２１－１２３４５６号公報",
  "・請求項　７\n・引用文献等　４\n・備考\nXXX\nXXX\n\n<引用文献等一覧>\n１．特開２０２０－１２３４５６号公報\n\n２．特開２０２１－１２３４５６号公報"
);

// 全角 ＜ 見出しでも終端
check(
  "山括弧見出し（全角）で終端",
  "・請求項\nXXX\n\nXXX\n\n＜拒絶の理由を発見しない請求項＞\n一覧\n\n一覧",
  "・請求項\nXXX\nXXX\n\n＜拒絶の理由を発見しない請求項＞\n一覧\n\n一覧"
);

// 山括弧見出しの後ろ（一覧本文）の空行は残る（上の2ケースでも検証済みだが明示）
check(
  "山括弧見出し以降は不変",
  "・請求項\nXXX\n\n<付記>\nこの付記の\n\n空行は残る",
  "・請求項\nXXX\n\n<付記>\nこの付記の\n\n空行は残る"
);

// ●理由 → ヘッダ群 → 本文 → 山括弧見出し（実文書の最終ブロック形）
check(
  "実文書形: ●理由開始 + 山括弧終端",
  "●理由１（進歩性）について\n・請求項　\n・引用文献等　Ｘ\n・備考\n本文１\n\n本文２\n\n本文３\n\n<拒絶の理由を発見しない請求項>",
  "●理由１（進歩性）について\n・請求項　\n・引用文献等　Ｘ\n・備考\n本文１\n本文２\n本文３\n\n<拒絶の理由を発見しない請求項>"
);

// ---------------------------------------------------------------------------
// 終端条件: 「-」「－」で始まる行（「<」「＜」と同種の行頭記号ルール）
// ---------------------------------------------------------------------------

check(
  "半角ハイフンで始まる行で終端",
  "・請求項\nXXX\n\nXXX\n\n------------------------------------\n<先行技術文献調査結果の記録>",
  "・請求項\nXXX\nXXX\n\n------------------------------------\n<先行技術文献調査結果の記録>"
);

check(
  "全角ハイフンで始まる行で終端",
  "・請求項\nXXX\n\nXXX\n\n－－－－－－－－－－\n＜付記＞",
  "・請求項\nXXX\nXXX\n\n－－－－－－－－－－\n＜付記＞"
);

check(
  "ハイフン始まり行以降は不変",
  "・請求項\nXXX\n\n------------------------------------\nこの行の\n\n空行は残る",
  "・請求項\nXXX\n\n------------------------------------\nこの行の\n\n空行は残る"
);

check(
  "行途中のハイフンは終端にならない",
  "・請求項\n段落[0001]-[0008]の記載\n\n続き\n\n●理由２について",
  "・請求項\n段落[0001]-[0008]の記載\n続き\n\n●理由２について"
);

// ---------------------------------------------------------------------------
// 見出し行（buildHeadingMarkRe = formatBody.isHeadingLine）の前は空行を 1 行残す
// ---------------------------------------------------------------------------

// 本文中の見出し行の前に空行が入る（他の空行は詰める）
check(
  "見出し行の前に空行を1行残す",
  "・請求項\nXXX\n\n(１)主張１について\n\nYYY\n\nZZZ\n\n●理由２について",
  "・請求項\nXXX\n\n(１)主張１について\nYYY\nZZZ\n\n●理由２について"
);

// 見出しの形式いろいろ（１． / 1) / 第１章）
check(
  "見出し形式: １．",
  "・請求項\nXXX\n\n１．検討\n\nYYY\n\n●理由２について",
  "・請求項\nXXX\n\n１．検討\nYYY\n\n●理由２について"
);
check(
  "見出し形式: 1)",
  "・請求項\nXXX\n\n1)検討\n\nYYY\n\n●理由２について",
  "・請求項\nXXX\n\n1)検討\nYYY\n\n●理由２について"
);
check(
  "見出し形式: 第１章",
  "・請求項\nXXX\n\n第１章　総論\n\nYYY\n\n●理由２について",
  "・請求項\nXXX\n\n第１章　総論\nYYY\n\n●理由２について"
);

// 複数の見出しがある場合、それぞれの前に空行
check(
  "複数見出しの前にそれぞれ空行",
  "・請求項\n(１)主張１\n\nAAA\n\n(２)主張２\n\nBBB\n\n●理由２について",
  "・請求項\n(１)主張１\nAAA\n\n(２)主張２\nBBB\n\n●理由２について"
);

// 本文の先頭行が見出しの場合はヘッダ群直後に空行を入れない（従来の直結形を維持）
check(
  "先頭本文が見出しでもヘッダ群直後は直結",
  "・請求項\n・備考\n(１)主張１について\n\nAAA\n\n●理由２について",
  "・請求項\n・備考\n(１)主張１について\nAAA\n\n●理由２について"
);

// 行頭に空白があっても見出しと判定される（HEADING_MARK_RE は行頭空白を許容）
check(
  "行頭空白つき見出しの前にも空行",
  "・請求項\nXXX\n\n　(１)主張１について\n\nYYY\n\n●理由２について",
  "・請求項\nXXX\n\n　(１)主張１について\nYYY\n\n●理由２について"
);

// 見出しでない行（数字が行頭でない）には空行を入れない
check(
  "見出しでない行には空行を入れない",
  "・請求項\n本文1.2の説明\n\n続き(1)を参照\n\n●理由２について",
  "・請求項\n本文1.2の説明\n続き(1)を参照\n\n●理由２について"
);

console.log(`test_claimsBlock: OK（${passed} ケース全て一致）`);
